<script lang="ts" setup>
import type { PhoneWidget } from '@strivacity/sdk-core';
import type { LoginContext } from '@strivacity/sdk-vue';
import { computed, inject } from 'vue';

const props = defineProps<{ formId: string; config: PhoneWidget }>();
const loginContext = inject<LoginContext>('loginContext');
const disabled = computed(() => !!loginContext?.loading.value || !!props.config.readonly);
const errorMessage = computed(() => loginContext?.messageContexts.value[props.formId]?.[props.config.id]?.text);
const validator = computed(() => props.config.validator);

function onChange(event: Event) {
	if (disabled.value) {
		return;
	}

	loginContext?.setFormState(props.formId, props.config.id, (event.target as HTMLInputElement).value);
}

async function onKeyDown(event: KeyboardEvent) {
	if (disabled.value) {
		return;
	}

	const input = event.target as HTMLInputElement;

	if (input.reportValidity()) {
		loginContext?.setFormState(props.formId, props.config.id, (event.target as HTMLInputElement).value);
		await loginContext?.submitForm(props.formId);
	}
}
</script>

<template>
	<div>
		<label v-if="config.label" :for="config.id" class="label">{{ config.label }}</label>
		<input
			:id="config.id"
			:name="config.id"
			:type="config.type"
			autocomplete="tel"
			inputmode="tel"
			:readonly="disabled"
			:required="validator?.required"
			size="1"
			@change="onChange($event)"
			@keydown.enter.stop="onKeyDown($event)"
		/>
		<small v-if="errorMessage" class="error">{{ errorMessage }}</small>
	</div>
</template>

